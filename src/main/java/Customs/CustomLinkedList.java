    private E unlink(Node<E> toUnlink) {
        final E element = toUnlink.item;
        final Node<E> prev = toUnlink.prev;
        final Node<E> next = toUnlink.next;

        if(prev == null) {
            first = next;
        }
        else {
            prev.next = next;
            toUnlink.prev = null;
        }
        if(next == null) {
            last = prev;
        }
        else {
            next.prev = prev;
            toUnlink.next = null;
        }
        toUnlink.item = null;
        size--;
        return element;
    }

